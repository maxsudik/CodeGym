package com.codegym.task.task03.task0309;

/* 
Sum of 5 numbers

*/

public class Solution {
    public static void main(String[] args) {
        //write your code here
        int result=0;
        for(int i=1; i<=5; i++){
            result += i;
            System.out.println(result);
        }

    }
}
